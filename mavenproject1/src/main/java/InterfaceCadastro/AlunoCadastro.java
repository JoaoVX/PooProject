    public void lertablealuno(){
        DefaultTableModel tablealuno = (DefaultTableModel) jTable1.getModel();
        CrudAlunos ca = new CrudAlunos();
        tablealuno.setNumRows(0);
        try {
            for(Alunos a : ca.SelectAlunos()){
                tablealuno.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getIdade(),
                a.getTurma(),
                a.getNomeresponsavel()
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(AlunoCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
