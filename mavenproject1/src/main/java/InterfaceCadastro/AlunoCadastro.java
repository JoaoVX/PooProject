    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jTable1.getSelectedRow() != -1){
            Alunos a = new Alunos();
            CrudAlunos ca = new CrudAlunos();
            a.setNome(txtNomeAluno.getText());
            a.setIdade(Integer.parseInt(txtIdadeAluno.getText()));
            a.setTurma(txtTurma.getText());
            a.setNomeresponsavel(txtResp.getText());
            a.setId((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            try {
                ca.UpdateAlunos(a);
                JOptionPane.showMessageDialog(null,"Atualizado com sucesso");
                txtNomeAluno.setText("");
                txtIdadeAluno.setText("");
                txtTurma.setText("");
                txtResp.setText("");
                lertablealuno();
            } catch (SQLException ex) {
                Logger.getLogger(AlunoCadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

