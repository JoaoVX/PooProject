    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Funcionario f = new Funcionario();
        CrudFuncionario cf = new CrudFuncionario();
        f.setCpf(txtCPFfunc.getText());
        f.setFuncao(txtFuncao.getText());
        f.setTurno(txtTurno.getText());
        f.setNome(txtNomefunc.getText());
        try {
            cf.InsertFuncionario(f);
            JOptionPane.showMessageDialog(null,"Salvo com sucesso");
            txtCPFfunc.setText("");
            txtFuncao.setText("");
            txtTurno.setText("");
            txtNomefunc.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(FuncionarioCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTable1.getSelectedRow() != -1){
            Funcionario f = new Funcionario();
            CrudFuncionario cf = new CrudFuncionario();
            f.setFuncao(txtFuncao.getText());
            f.setTurno(txtTurno.getText());
            f.setNome(txtNomefunc.getText());
            f.setCpf((String) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            try {
                cf.UpdateFuncionario(f);
                JOptionPane.showMessageDialog(null,"Atualizado com sucesso");
                txtCPFfunc.setText("");
                txtFuncao.setText("");
                txtTurno.setText("");
                txtNomefunc.setText("");
                lertablefunc();
            } catch (SQLException ex) {
                Logger.getLogger(FuncionarioCadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

