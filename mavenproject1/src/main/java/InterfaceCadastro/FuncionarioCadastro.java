    public void lertablefunc(){
        DefaultTableModel tablefunc = (DefaultTableModel) jTable1.getModel();
        CrudFuncionario cf = new CrudFuncionario();
        tablefunc.setNumRows(0);
        try {
            for(Funcionario f : cf.SelectFuncionario()){
                tablefunc.addRow(new Object[]{
                    f.getCpf(),
                    f.getFuncao(),
                    f.getTurno(),
                    f.getNome()
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(FuncionarioCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
